// Dynamically generate categories based on available games
const allGames = [...featuredGames, ...newGames, ...trendingGames, ...studentGames];
const usedCategories = [...new Set(allGames.map(game => game.category))];

export const categories = usedCategories.map((categoryName, index) => {
    const config = CATEGORY_CONFIG[categoryName] || DEFAULT_CATEGORY_STYLE;
    return {
        id: index + 1,
        name: categoryName,
        icon: config.icon,
        color: config.color,
    };
}).sort((a, b) => a.name.localeCompare(b.name)); // Optional: Sort alphabetically
